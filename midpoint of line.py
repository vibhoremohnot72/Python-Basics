x1 = float(input("Enter value of first x-co-ordinate: "))
y1 = float(input("Enter value of first y-co-ordinate: "))
x2 = float(input("Enter value of second x-co-ordinate: "))
y2 = float(input("Enter value of second y-co-ordinate: "))
m1 = (x1 + x2) / 2
m2 = (y1 + y2) / 2
print ("Midpoint Co-ordinate is: ", (m1 , m2))